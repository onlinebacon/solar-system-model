export const D1 = 1;
export const D90 = 90;
export const D180 = 180;
export const D360 = 360;

export const sin = (deg) => Math.sin(deg*(Math.PI/180));
export const cos = (deg) => Math.cos(deg*(Math.PI/180));

export const asin = (deg) => Math.asin(deg)*(180/Math.PI);
export const acos = (deg) => Math.acos(deg)*(180/Math.PI);
